/* En este ejemplo el usuario tecleará dos números. Debe devolver 
la diferencia entre el mayor y el menor.
*Si el usuario entre el 5 y el 8, el programa devolverá 3 (8 - 5)*
 */

let firstNumber = parseFloat(prompt('Ingrese el primer numero: '));
let secondNumber = parseFloat(prompt('Ingrese el segundo numero:'));

alert(`La diferencia entre ${firstNumber} y ${secondNumber} es : ${firstNumber - secondNumber}`)

